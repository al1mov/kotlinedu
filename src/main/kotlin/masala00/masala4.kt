package masala00

fun main() {
    val day = readLine()!!.toInt()
    when(day) {
        1 -> {
            println("Dushanba")
        }
        2 -> {
            println("Seshanba")
        }
        3 -> {
            println("Chorchanba")
        }
        4 -> {
            println("Payshanba")
        }
        5 -> {
            println("Juma")
        }
        6 -> {
            println("Shanba")
        }
        7 -> {
            println("Yakshanba")
        }
        else -> {
            println("Hapshanba")
        }
    }
}